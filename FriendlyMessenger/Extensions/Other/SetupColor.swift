import UIKit

class SetupColor {
    static func blackColor() -> UIColor {
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    static func secondaryBlackColor() -> UIColor {
        return #colorLiteral(red: 0.1098039216, green: 0.1098039216, blue: 0.1137254902, alpha: 1)
    }
    
    static func whiteColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func secondaryWhiteColor() -> UIColor {
        return #colorLiteral(red: 0.5960784314, green: 0.5960784314, blue: 0.6196078431, alpha: 1)
    }
    
    static func separationColor() -> UIColor {
        return #colorLiteral(red: 0.2392156863, green: 0.2392156863, blue: 0.2509803922, alpha: 1)
    }
}
